package casanovaProject;

public abstract class BaseInventoryItem implements IInventoryItem {
    protected String id;
    protected String name;
    protected double price;
    protected int quantity;
    protected String category;

    public BaseInventoryItem(String id, String name, double price, int quantity, String category) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.category = category;
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public double getPrice() {
        return price;
    }

    @Override
    public int getQuantity() {
        return quantity;
    }

    @Override
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    @Override
    public String getCategory() {
        return category;
    }
} 